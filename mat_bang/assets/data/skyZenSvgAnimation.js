// Helper function để tính toán kích thước font dựa trên kích thước path
const calculateFontSize = (pathElement) => {
  const bbox = pathElement.getBBox();
  const pathWidth = bbox.width;
  const pathHeight = bbox.height;

  // Lấy kích thước nhỏ hơn giữa width và height để đảm bảo text vừa trong path
  const minDimension = Math.min(pathWidth, pathHeight);

  // Font size = 30% của kích thước nhỏ nhất
  const fontSize = Math.max(minDimension * 0.3, 80); // Tối thiểu 12px

  return Math.round(fontSize);
};

// Helper function để tạo SVG với active paths và thêm text
export const createSvgWithActivePaths = (svgTemplate, activePathIds = []) => {
  // Parse SVG template
  const temp = document.createElement('div');
  temp.innerHTML = svgTemplate;
  const svg = temp.firstElementChild;

  // Lấy tất cả paths trong SVG để tính tổng số
  const allPaths = svg.querySelectorAll('path');
  const totalPaths = allPaths.length;

  // Tạo map để lưu thứ tự của từng path
  const pathOrderMap = new Map();
  allPaths.forEach((path, index) => {
    const pathId = path.getAttribute('id');
    pathOrderMap.set(pathId, index + 1); // Thứ tự bắt đầu từ 1
  });

  // Update paths based on active state
  const paths = svg.querySelectorAll('path');
  paths.forEach((path) => {
    const pathId = path.getAttribute('id');
    const isActive = activePathIds.includes(pathId);

    if (isActive) {
      // Giữ nguyên màu gốc cho active paths
      // Không thay đổi màu
    } else {
      // Đổi thành màu xám cho inactive paths
      path.setAttribute('fill', '#D3D3D3'); // Light gray
      path.setAttribute('stroke', '#A9A9A9'); // Darker gray
      path.setAttribute('fill-opacity', '0.3');
    }
  });

  // Thêm text với số thứ tự vào các path active
  if (activePathIds.length > 0) {
    activePathIds.forEach((pathId) => {
      const path = svg.querySelector(`path[id="${pathId}"]`);
      if (path) {
        // Lấy vị trí text từ map
        const position = getTextPositionByPathId(pathId);

        // Tính toán font size dựa trên kích thước path
        const fontSize = calculateFontSize(path);

        // Lấy thứ tự của path này
        const pathOrder = pathOrderMap.get(pathId) || 1;

        // Tạo text element với số thứ tự của path
        const text = document.createElementNS(
          'http://www.w3.org/2000/svg',
          'text',
        );
        text.setAttribute('x', position.x);
        text.setAttribute('y', position.y);
        text.setAttribute('text-anchor', 'middle');
        text.setAttribute('dominant-baseline', 'middle');
        text.setAttribute('fill', '#FF0000'); // Màu đỏ
        text.setAttribute('font-size', fontSize);
        text.setAttribute('font-weight', 'bold');
        text.setAttribute('pointer-events', 'none'); // Không cho phép click vào text
        text.textContent = pathOrder.toString(); // Hiển thị thứ tự của path

        // Thêm text vào SVG
        svg.appendChild(text);
      }
    });
  }

  return svg.outerHTML;
};

